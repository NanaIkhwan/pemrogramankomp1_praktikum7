    private void LoginNow() {
        String user = txtUsername.getText();
        String pass = new String(txtPassword.getPassword());

        String q = "SELECT * FROM user "
                + "WHERE username='" + user + "' "
                + "AND password='" + pass + "'";
//        System.out.println(q);

        try {
            Connection K = koneksi.konek();
            Statement S = K.createStatement();
            ResultSet R = S.executeQuery(q);
            
            int x =0;
            String level = "";
            while (R.next()) {                
                int id = R.getInt("id");
                String fullname = R.getString("fullname");
                String username = R.getString("username");
                String password = R.getString("password");
                level = R.getString("level");
                x++;
            }
            
            if(x > 0){
                //login sukses
                CurrentUser.username = user;
                JOptionPane.showMessageDialog(this, "Login successfully"); 
                
                if(level.equals("owner")){
                    //owner
                    OwnerPage op = new OwnerPage();
                    op.setVisible(true); 
                    this.setVisible(false); 
                    op.setExtendedState(Frame.MAXIMIZED_BOTH); 
                }else if(level.equals("admin")){
                    //admin
                    AdminPage ap = new AdminPage();
                    ap.setVisible(true); 
                    this.setVisible(false); 
                    ap.setExtendedState(Frame.MAXIMIZED_BOTH); 
                }else if(level.equals("kasir")){
                    //kasir
                    KasirPage kp = new KasirPage();
                    kp.setVisible(true); 
                    this.setVisible(false); 
                    kp.setExtendedState(Frame.MAXIMIZED_BOTH); 
                }else {
                    //
                }
                
            }else {
                //login gagal
                JOptionPane.showMessageDialog(this, "Invalid username/password"); 
            }
        } catch (SQLException e) {
            //error handling
        }

    }
